sp_1 = [int(input(f"введите {i+1} элемент из {n}: ")) for i in range(n)]
sp_2 = [int(input(f"введите {i+1} элемент из {m}: ")) for i in range(m)]
mn_1 = set(sp_1)
mn_2 = set(sp_2)
lst = [i for i in mn_1 if i in mn_2]
