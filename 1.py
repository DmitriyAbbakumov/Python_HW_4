# Выдать без повторений в порядке возрастания все те числа, которые встречаются в обоих наборах. 

# Пользватель вводит 2 числа. n - кол-во элементов первого множества. m - кол-во элементов второго множества. 
# Затем пользователь вводит сами элементы множеств.

# 11 6 
# 2 4 6 8 10 12 10 8 6 4 2 
# 3 6 9 12 15 18 
# 6 12


n = int(input("Введите кол-во эл-тов первого списка: "))
